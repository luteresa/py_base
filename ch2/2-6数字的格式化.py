#format函数
#参数1：要格式化的数字
#参数2：格式化字符串

x=1234.56789

#保留小数点后两位,1234.57
print(format(x,'0.2f'))

#保留小数点后1位，并且数字在12个字符串长度区域内右对齐，左侧保留空格
#'      1234.6'
print(format(x,'12.1f')) #默认大于号

print(format(x,'0>12.1f')) #补零

#保留小数点后1位，并且数字在12个字符串长度区域内左对齐，右侧保留空格
#‘1234.57     12
print(format(x,'<12.2f'),12)
print(format(x,'0<12.2f'),12)


print(format(x,'0^12.2f'),12)  #中间对齐，补零

#加上千分号
#1,234.56789
print(format(x,',.2f'))

#科学计数法
#1110=1.11E3
print(format(x,'e'))
print(format(x,'0.2E')) #保留两位小数
